
public class STUDYVARIABLE {

	public static void main(String[] args) {
		// 변수는 일종의 박스입니다.
		// 이 박스에는 넣을 물건의 종류와, 박스의 이름을 정해야 합니다.
		// 지금부터 a라는 박스에 5라는 물건을 담을 예정입니다.
		// 그러기 위해 a라는 박스를 먼저 생성해줍니다.
		int a;
		// 다음 5를 집어 넣습니다
		a = 5;
		// 그 후 a의 값을 콘솔에 찍습니다.
		System.out.println(a);
		// 한번에 여러개를 선언할 수도 있습니다
		int A, B, C;
		A = 10;
		B = 20;
		C = 30;
		System.out.println(A);
		System.out.println(B);
		System.out.println(C);
		// a의 있던 값을 갱신 할 수도 있습니다.
		a = 25;
		System.out.println(a);

	}

}
