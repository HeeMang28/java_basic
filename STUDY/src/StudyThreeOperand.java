
public class StudyThreeOperand {

	public static void main(String[] args) {
		// 3항연산자는 (조건식 ? 참일때 결과 : 거짓일때결과) 형식으로 작성합니다.
		// 조건식의 결과가 true라면 참일때결과 에 적어적은 값이 출력되고
		// 조건식의 결과가 false라면 거짓일때결과 로 적어넣은것이 출력됩니다.
		System.out.println((7 < 5 ? "7은 5보다 작습니다." : "7은 5보다 큽니다."));
		//System.out.println("7은 5보다 큽니다.");
	}

}
