
public class Variable01P30 {

	public static void main(String[] args) {
		// 변수는 일종의 박스입니다.
		// 이 박스에는 넣을 물건의 종류와, 박승명칭을 부여해야 합니다.
		
		// 아래는 "정수"라는 물건이 a라는 이름의 박스에 담길 예정이라는 의미입니다.(아직 안 담음)
		int a;
		// a박스에 5를 집어넣습니다.
		a = 5;
		// a박스에 들어있는 값을 콘솔에 찍어봅니다. (a에 따옴표를 안붙입니다.)
		System.out.println(a);

		// 아래는 "정수"라는 물건이 A라는 이름의 박스에 담길 예정이라는 의미입니다.(아직 안 담음)
		int A;
		// A박스에 10을 집어넣습니다.
		A = 10;
	    System.out.println(A);
	}

}
